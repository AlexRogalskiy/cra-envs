#!/usr/bin/env node


import { join as pathJoin, basename as pathBasename } from "path";
import { getEnvNames } from "./getEnvNames";
import * as fs from "fs";
import { getProjectRoot } from "../tools/getProjectRoot";

const targetProjectDirPath = process.cwd();

fs.writeFileSync(
    pathJoin(targetProjectDirPath, "src", "getEnv.ts"),
    Buffer.from(
        [
            `// Generated by ${pathBasename(__filename).split(".")[0]} do not edit`,
            `import { getEnvFactory } from "${require(pathJoin(getProjectRoot(), "package.json"))["name"]}";`,
            '',
            `export const { getEnv } = getEnvFactory<${getEnvNames({ targetProjectDirPath })
                .map(envName => `"${envName}"`)
                .join(" | ")
            }>();`,
            ''
        ].join("\n"),
        "utf8"
    )
);






